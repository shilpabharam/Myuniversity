    REFERENCES `MYUNIVERSITY`.`SKILL_BADGES` (`ID`)
    ON DELETE NO ACTION
    ON UPDATE NO ACTION,
  CONSTRAINT `CRITICAL_THINKING`
    FOREIGN KEY (`CRITICAL_THINKING_MEDAL`)
    REFERENCES `MYUNIVERSITY`.`SKILL_BADGES` (`ID`)
    ON DELETE NO ACTION
    ON UPDATE NO ACTION,
  CONSTRAINT `PROFESSIONALISM`
    FOREIGN KEY (`PROFESSIONALISM_MEDAL`)
    REFERENCES `MYUNIVERSITY`.`SKILL_BADGES` (`ID`)
    ON DELETE NO ACTION
    ON UPDATE NO ACTION,
  CONSTRAINT `RESEARCH_ANALYSIS`
    FOREIGN KEY (`REASEARCH_ANALYSIS_MEDAL`)
