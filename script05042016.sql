  INDEX `EFFECTIVE_COMMU_idx` (`EFFECTIVE_COMMUNICATION_MEDAL` ASC),
  INDEX `COLLABORATION_idx` (`COLLABORATION_MEDAL` ASC),
  INDEX `CRITICAL_THINKING_idx` (`CRITICAL_THINKING_MEDAL` ASC),
