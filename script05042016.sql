  `CRITICAL_THINKING` VARCHAR(100) NULL,
  `CRITICAL_THINKING_MEDAL` BIGINT(25) NULL,
  `PROFESSIONALISM` VARCHAR(100) NULL,
  `PROFESSIONALISM_MEDAL` BIGINT(25) NULL,
  `REASEARCH_ANALYSIS` VARCHAR(100) NULL,
  `REASEARCH_ANALYSIS_MEDAL` BIGINT(25) NULL,
