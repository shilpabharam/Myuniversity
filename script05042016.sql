  `ID` BIGINT(25) NOT NULL AUTO_INCREMENT,
  `LOBO_BADGE_NAME` VARCHAR(100) NULL,
  `EFFECTIVE_COMMUNICATION` VARCHAR(100) NULL,
  `EFFECTIVE_COMMUNICATION_MEDAL` BIGINT(25) NULL,
  `COLLABORATION` VARCHAR(100) NULL,
